dado = input('Qual o seu nome e sua idade? ')

print('Qual é o tipo primitivo?')
print(type(dado))       
print('É um numeral?')
print(dado.isnumeric())
print('É uma letra?')
print(dado.isalpha())
print('É alfanumerico?')                                                         
print(dado.isalnum())
print('Todas as letras são maiúsculas?')
print(dado.isupper())
print('Todas as letras são minúsculas?')
print(dado.islower())
