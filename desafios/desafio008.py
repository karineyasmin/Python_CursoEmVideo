metros = float(input('Digite o valor em metros: '))


print(f'\nVocê digitou {metros} em metros') 
print(f'\n{metros} é equivalente a {metros * 100} centímetros')
print(f'\n{metros} é equivalente a {metros * 1000} milímetros ')